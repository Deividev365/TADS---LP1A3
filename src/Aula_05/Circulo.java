package Aula_05;

public class Circulo {
	
	
	 private double raioCirculo;
	 private String corCirculo;


	
	public String getCorCirculo() {
		return corCirculo;
	}


	public void setCorCirculo(String corCirculo) {
		this.corCirculo = corCirculo;
	}


	public double getRaioCirculo() {
		return raioCirculo;
	}


	public void setRaioCirculo(double raioCirculo) {
		this.raioCirculo = raioCirculo;
	}


	






public double setCalcCirculo(double x) {
		
		
		
		return Math.PI * Math.pow(x, 2);
		
	}
	
	
	
	
	
	
}
