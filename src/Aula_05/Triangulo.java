package Aula_05;

public class Triangulo {

	
	private String corTriangulo;
	private double baseTriangulo;
	private double alturaTriangulo;
	
	
	
	
	
	public String getCorTriangulo() {
		return corTriangulo;
	}


	public void setCorTriangulo(String corTriangulo) {
		this.corTriangulo = corTriangulo;
	}


	public double getBaseTriangulo() {
		return baseTriangulo;
	}


	public void setBaseTriangulo(double baseTriangulo) {
		this.baseTriangulo = baseTriangulo;
	}


	public double getAlturaTriangulo() {
		return alturaTriangulo;
	}


	public void setAlturaTriangulo(double alturaTriangulo) {
		this.alturaTriangulo = alturaTriangulo;
	}




	public double setCalcTriangulo(double x, double y) {
		
		
		
		return x * y;
	}
	
		
}
